let sunglass = document.querySelector(".cardcarogry1");
let eyeglass = document.querySelector(".cardcarogry2");

function ShowSunglass() {
    sunglass.style.display = 'block';
    eyeglass.style.display = 'none';
}

function ShowEyeglass() {
    eyeglass.style.display = 'block';
    sunglass.style.display = 'none';
}